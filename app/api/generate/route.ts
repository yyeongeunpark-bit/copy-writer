    // 여기 catch 블록으로 넘어옵니다.

    // 개발자(우리)가 나중에 문제를 추적할 수 있도록,
    // Vercel의 로그(기록)에 에러 내용을 남겨두는 부분이에요.
    // 사용자 화면에는 안 보이고, 우리가 Vercel 대시보드에서만 확인할 수 있어요.
