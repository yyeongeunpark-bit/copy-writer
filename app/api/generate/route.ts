    // 사용자 화면에는 "제미나이 연결 중 에러가 발생했습니다: (구체적인 이유)"
    // 라는 형태로 알기 쉽게 에러 메시지를 보여줍니다.
    // status: 500은 "서버 쪽에서 문제가 생겼다"는 표준 신호예요.
